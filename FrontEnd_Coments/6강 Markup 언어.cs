[ 태그를 사용해 Markup 하기 ]
<b>무궁화</b>

태그의 기호 ( < > )가 마크업 기호로 적합한 이유 :
아래처럼 사용하면 콘텐츠로 사용할 수가 없다.
1. 하느님이 보우하사 우리나라 만세 [ 무궁화 ] 삼천리 화려강산
2. 하느님이 보우하사 우리나라 만세 , 무궁화 , 삼천리 화려강산
3. 하느님이 보우하사 우리나라 만세 { 무궁화 } 삼천리 화려강산

콘텐츠 사용 
<b></b> == bold 체로 변경하겠다.
<i></i> == 여러개가 들어오더라도 출력을 해주겠다.
1. 하느님이 <i>보우하사 우리나라 만세 <b> 무궁화 </b> 삼천리 화려</i>강산


